void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  static uint8_t word_count = 0;
  if (rx_data[0] == ' ' || rx_data[0] == '\r' || rx_data[0] == '\n') { // end of word
    // process received word
    if (word_count > 0) {
      HAL_UART_Transmit_DMA(&huart1, tx_data, sizeof(tx_data));
      while (!tx_complete) {
        // wait for transmission to complete
      }
      tx_complete = 0;
    }
    word_count = 0;
  } else {
    tx_data[word_count] = rx_data[0];
    word_count++;
    if (word_count == sizeof(tx_data)) {
      // word too long: truncate and print
      HAL_UART_Transmit_DMA(&huart1, tx_data, sizeof(tx_data));
      while (!tx_complete) {
        // wait for transmission to complete
      }
      tx_complete = 0;
      word_count = 0;
    }
  }
  HAL_UART_Receive_DMA(&huart1, rx_data, 1);
}

void enqueue(uint8_t data) {
  uint8_t next_rear = (rx_rear + 1) % QUEUE_SIZE;
  if (next_rear != rx_front) {
    if (overwrite_mode && rx_rear == rx_front) {
      // overwrite mode: replace oldest data with space
      rx_queue[rx_rear] = ' ';
      rx_front = (rx_front + 1) % QUEUE_SIZE;
      overwrite_mode = 0;
    }
    rx_queue[rx_rear] = data;
    rx_rear = next_rear;
  } else {
    // queue full: set overwrite mode
    overwrite_mode = 1;
  }
}

uint8_t dequeue() {
  if (rx_front != rx_rear) {
    uint8_t data = rx_queue[rx_front];
    rx_front = (rx_front + 1) % QUEUE_SIZE;
    return data;
  }
  return 0;
}
